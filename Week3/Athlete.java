public class Athlete {
    public String name;
    public double time;

    public Athlete(String name, double time) {
        this.name = name;
        this.time = time;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getTime() {
        return this.time;
    }

    public void setTime(double time) {
        this.time = time;
    }

}
